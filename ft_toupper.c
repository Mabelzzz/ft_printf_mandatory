#include "ft_printf.h"

char	*ft_toupper(char *upper)
{
	int	i;
	
	i = 0;
	while (upper[i] != 0)
	{
		if (upper[i] >= 'a' && upper[i] <= 'z')
			upper[i] -= 32;
		i++;
	}
	return (upper);
}
